<div class="paper_title"><h3>Intro to Information Technology</h3></div>
<div id="structure_bar_img"></div>
<div class="paper_purpose">
<h3>Purpose / Aim</h3>
To introduce students to computer systems, and related Information Technology (IT) issues.
<br />
<br />
<h3>Learning Outcomes</h3>
<ol id="intent">
<li>Understand and effectively use various features of an operating system.</li>
<li>Understand and explain how IT is used in a home or small business environment.</li>
<li>Identify and describe the main components and peripherals of a computer system.</li>
<li>Identify threats to a computer system and how these can be mitigated.</li>
<li>Understand and describe health and ergonomic issues concerning IT</li>
<li>Develop an understanding of the impact of IT on society, both historically, at present and in the future</li>
</ol>
