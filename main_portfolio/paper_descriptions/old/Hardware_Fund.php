<h3>Purpose / Aim</h3>	
To provide students with a good working knowledge of information technology hardware.
<br />
<br />
<h3>Learning Outcomes</h3>
<ol id="intent">
<li>Describe a wide selection of hardware components and explain their functions, including selection issues and managing peripherals.</li>
<li>Correctly identify and configure a selection of information technology hardware resources.</li>
<li>Demonstrate and describe preventative maintenance of information technology hardware.</li>
<li>Explain the current and emerging trends in information technology hardware.</li>
</ol>
