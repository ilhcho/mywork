<div class="paper_title"><h3>Mutimedia & Website Develpment</h3></div>
<div id="structure_bar_img"></div>
<div class="paper_purpose">
<h3>Purpose / Aim</h3>
To provide the students with knowledge of the concepts of the Internet as well as website design and development and the ability to create a multimedia application for CD ROM or Website.
<br />
<br />
<h3>Learning Outcomes</h3>

<ol id="intent">
<li>Explain ownership, copyright issues associated with Multimedia and Internet Web sites. Explain Website Engine Optimisation.</li>
<li>Explain the fundamentals of screen design for Web pages and Multimedia applications.</li>
<li>Describe the fundamental multimedia and Web page elements and explain the associated terminology.</li>
<li>Create original media elements and explain the process followed.</li>
<li>Use the features of a Multimedia authoring package and explain the process followed.</li>
<li>Analyse the technical issues involved in development, hosting, usability, accessibility and website usage statistics.</li>
</ol>
