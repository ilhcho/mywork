<h3>Purpose / Aim</h3>
To provide students with an introduction to database systems.
<br />
<br />
<h3>Learning Outcomes</h3>
<ol id="intent">
<li>Describe different types of data, system data objects, and operations on data.</li>
<li>Create database designs to solve given business problems.</li>
<li>Implement database designs to solve given business problems in a database using an SQL software package.</li>
<li>Create SQL queries to solve given business problems.</li>
<li>Explain the meaning of the terms used in a database management environment.</li>
</ol>
