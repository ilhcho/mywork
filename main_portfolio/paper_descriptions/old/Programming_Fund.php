<h3>Purpose / Aim</h3>
To introduce students to the basic principles of designing and developing small computer programs within the context of programming language.
<br />
<br />
<h3>Learning Outcomes</h3>
<ol id="intent">
<li>Design small computer programs as solutions to problems of low complexity.</li>
<li>Implement the designs by writing well-structured programmes that follow enforced programming language conventions and programming standards.</li>
<li>Test, debug and document small computer programs.</li>
</ol>
